const mapperPrice = (price = "") => {
  price = price.split(".");
  return price.reduce(
    (t, v, index, old) => (t += index === old.length - 1 ? v.replace(/ đ/, "") : v),
    ""
  );
};

const mapperData = (data, dataDetails, type) => {
  return data.map((item, index) => {
    return {
      product_name: item.name,
      product_price: +mapperPrice(item.price),
      product_size: item.size,
      product_desc: dataDetails[index].description,
      product_images: [
        dataDetails[index].image1,
        dataDetails[index].image2,
        dataDetails[index].image3,
        dataDetails[index].image4,
      ],
      product_thumb: item.image,
      product_hover: item.hoverImage,
      product_type: type,
    };
  });
};

